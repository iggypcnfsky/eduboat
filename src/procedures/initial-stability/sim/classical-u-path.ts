/** Scale normalized outline: below wlFrac → draft, above → freeboard (keel at norm z=0, deck at norm z=1). */
export function mapNormZToDraftFreeboard(
  zNorm: number,
  wlFrac: number,
  draft: number,
  freeboard: number,
): number {
  if (zNorm <= wlFrac) {
    return wlFrac > 1e-9 ? (zNorm / wlFrac) * draft : 0
  }
  const topFrac = 1 - wlFrac
  return draft + (topFrac > 1e-9 ? ((zNorm - wlFrac) / topFrac) * freeboard : 0)
}

/** Normalized z of the design waterline on CLASSICAL_U_NORM (from default draft / depth ratio). */
const CLASSICAL_U_WL_FRAC = 1.25 / (1.25 + 0.65)

/** Scale normalized lesson SVG outline to body frame (K at keel, WL at draft, deck at draft+freeboard). */
export function buildClassicalUOutline(p: HullParams): Vec2[] {
  const half = p.beam / 2
  return CLASSICAL_U_NORM.map(({ x, z }) => ({
    x: x * half,
    z: mapNormZToDraftFreeboard(z, CLASSICAL_U_WL_FRAC, p.draft, p.freeboard),
  }))
}
